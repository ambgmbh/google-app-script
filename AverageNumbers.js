// Bibliothek zum Berechnen des Durchschnitts einer Zahlenreihe
Library: AverageNumbers

/**
 * Berechnet den Durchschnitt einer Reihe von Zahlen.
 *
 * @param {number[]} numbers Die Zahlenreihe.
 * @returns {number} Den Durchschnitt der Zahlen.
 */
function averageNumbers(numbers) {
  var sum = 0;
  for (var i = 0; i < numbers.length; i++) {
    sum += numbers[i];
  }
  return sum / numbers.length;
}
